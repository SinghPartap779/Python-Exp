cells.append(md(L(
    "---",
    "## 9. Short-Circuit Evaluation",
    "",
    "Python **stops evaluating** a logical expression as soon as the outcome is determined:",
    "",
    "- `and`: if the **left** side is **False**, the right side is never evaluated",
    "  (False AND anything = False)",
    "- `or`: if the **left** side is **True**, the right side is never evaluated",
    "  (True OR anything = True)",
    "",
    "**Why it matters:**",
    "1. **Performance** — skip expensive operations when not needed",
    "2. **Safety** — avoid errors (e.g., check if a list is non-empty before indexing it)",
    "",
    "```python",
    "# Safe: the right side only runs if items is non-empty",
    "if items and items[0] == 'apple':",
    "    ...",
    "",
    "# Unsafe without short-circuit — would crash on empty list:",
    "# if items[0] == 'apple':   ← IndexError if items is []",
    "```",
)))

cells.append(code(L(
    "# Demonstrating short-circuit — the function shows when it's called",
    "",
    "def expensive_check():",
    '    print("  [expensive_check] was called")',
    "    return True",
    "",
    'print("--- and short-circuit ---")',
    'print("Left=False, right side skipped:")',
    "result = False and expensive_check()   # expensive_check NOT called",
    'print("result:", result)',
    "",
    'print("\\nLeft=True, right side evaluated:")',
    "result = True and expensive_check()    # expensive_check IS called",
    'print("result:", result)',
    "",
    'print("\\n--- or short-circuit ---")',
    'print("Left=True, right side skipped:")',
    "result = True or expensive_check()     # expensive_check NOT called",
    'print("result:", result)',
)))


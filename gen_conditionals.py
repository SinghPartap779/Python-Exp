    "## Key Takeaways",
    "",
    "| Concept | What to remember |",
    "|---------|-----------------|",
    "| `if` | Runs a block when condition is True |",
    "| `elif` | Only checked if all previous conditions were False |",
    "| `else` | Catches everything — no condition |",
    "| Ternary | `x if cond else y` — one-liner for simple assignments |",
    "| Falsy | `0, '', [], {}, None, False` all behave as False |",
    "| `==` vs `is` | `==` compares values; `is` compares object identity |",
    "| `and` | Both must be True; stops at first False (short-circuit) |",
    "| `or` | One must be True; stops at first True (short-circuit) |",
    "| Guard clauses | Check failures first, return early, keep happy path flat |",
    "| `match` | Pattern matching (Python 3.10+); cleaner than many `elif`s |",
    "",
    "### Common Mistakes to Avoid",
    "1. **`=` vs `==`**: `if x = 5` is a syntax error; use `if x == 5`",
    "2. **Forgetting the colon**: `if x > 0` → must be `if x > 0:`",
    "3. **Wrong indentation**: Python uses indentation to define blocks — be consistent",
    "4. **Using `is` for value comparison**: `if x is 5` — use `if x == 5`",
